# 숫자 자료형
print(5) # 5 출력
print(-10) # -10 출력
print(3.14) # 3.14 출력
print(1000) # 1000 출력
print(5+3) # 8 출력
print(2*8) # 16 출력 (주의 : x 가 아닌 * 예요)
print(3*(3+1)) # 12 출력

# 문자 자료형
print('풍선') # 작은 따옴표로 감싼 문자열 
print("나비") # 큰 따옴표로 감싼 문자열 
print("ㅋㅋㅋㅋㅋㅋㅋㅋㅋ") # 긴 문자열 
print("ㅋ"*9) # ㅋ 을 9번 출력 

# boolean 자료형
print(5 > 10) # False 출력 
print(5 < 10) # True 출력 
print(True) # True 
print(False) # False 
print(not True) # False 출력 
print(not False) # True 출력 
print(not (5 > 10)) # True 출력 